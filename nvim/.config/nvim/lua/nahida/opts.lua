local opt = vim.opt

-- context
opt.number = true
opt.relativenumber = true
opt.scrolloff = 8
opt.signcolumn = 'yes'
opt.errorbells = false

-- encoding
opt.encoding = 'utf8'
opt.fileencoding = 'utf8'

-- indent
opt.tabstop = 4
opt.softtabstop = 4
opt.shiftwidth = 4
opt.expandtab = true
opt.smarttab = true
opt.smartindent = true
opt.wrap = false

-- search
opt.hlsearch = false
opt.incsearch = true
opt.ignorecase = true
opt.smartcase = true

-- appearance
opt.syntax = 'on'
opt.termguicolors = true
opt.cmdheight = 1
